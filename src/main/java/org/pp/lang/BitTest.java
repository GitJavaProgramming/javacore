public class BitTest {
    public void test01() {
        // 二进制取反码/按位取反  ~(1000) = -1001  ~8=-9
        // 0000 1000 -> 1111 0111 -> 1111 1000(符号位不变) -> 1111 1001(反码+1) 高位为1 为负数
        System.out.println("~8 = " + ~(1000));  // -9
        System.out.println("~(128) = " + ~(128));
        System.out.println("~(129) = " + ~(129));
    }
}
